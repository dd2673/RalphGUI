#!/usr/bin/env python3
"""
Ralph Setup - Create new Ralph project with .ralph/ subfolder structure.
"""

import argparse
import os
import shutil
import subprocess
import sys
from datetime import datetime
from pathlib import Path


def log(level: str, message: str):
    """Print colored log message."""
    colors = {
        "INFO": "\033[94m",     # Blue
        "WARN": "\033[93m",     # Yellow
        "ERROR": "\033[91m",    # Red
        "SUCCESS": "\033[92m",  # Green
    }
    reset = "\033[0m"
    color = colors.get(level, "")
    timestamp = datetime.now().strftime("%H:%M:%S")
    print(f"{color}[{timestamp}] [{level}] {message}{reset}")


def find_templates_dir(arg_templates_dir: Path = None) -> Path | None:
    """Find templates directory.

    Priority:
    1. --templates-dir argument
    2. ../templates (relative to project)
    3. ~/.ralph/templates (home directory)
    """
    if arg_templates_dir:
        if arg_templates_dir.exists():
            return arg_templates_dir
        log("ERROR", f"Templates directory not found: {arg_templates_dir}")
        return None

    # Check ../templates relative to current directory
    local_templates = Path.cwd().parent / "templates"
    if local_templates.exists():
        return local_templates

    # Check ~/.ralph/templates
    home_templates = Path.home() / ".ralph" / "templates"
    if home_templates.exists():
        return home_templates

    return None


def verify_templates(templates_dir: Path) -> bool:
    """Verify required template files exist."""
    required = ["PROMPT.md", "fix_plan.md", "AGENT.md"]
    missing = []
    for fname in required:
        if not (templates_dir / fname).exists():
            missing.append(fname)

    if missing:
        log("ERROR", f"Required template files missing: {', '.join(missing)}")
        return False
    return True


def create_project_structure(project_path: Path) -> None:
    """Create Ralph project directory structure."""
    dirs = [
        project_path / "src",
        project_path / ".ralph" / "specs" / "stdlib",
        project_path / ".ralph" / "examples",
        project_path / ".ralph" / "logs",
        project_path / ".ralph" / "docs" / "generated",
    ]
    for d in dirs:
        d.mkdir(parents=True, exist_ok=True)


def copy_templates(project_path: Path, templates_dir: Path) -> None:
    """Copy template files to .ralph/ directory."""
    # Main templates
    for fname in ["PROMPT.md", "fix_plan.md", "AGENT.md"]:
        src = templates_dir / fname
        dst = project_path / ".ralph" / fname
        if src.exists():
            shutil.copy2(src, dst)
            log("INFO", f"Copied {fname} to .ralph/")

    # Copy specs directory contents
    specs_src = templates_dir / "specs"
    specs_dst = project_path / ".ralph" / "specs"
    if specs_src.exists():
        for item in specs_src.iterdir():
            if item.is_file():
                shutil.copy2(item, specs_dst / item.name)
            elif item.is_dir():
                # Copy directory contents
                target = specs_dst / item.name
                target.mkdir(exist_ok=True)
                for subitem in item.rglob("*"):
                    if subitem.is_file():
                        rel_path = subitem.relative_to(item)
                        dest_file = target / rel_path
                        dest_file.parent.mkdir(parents=True, exist_ok=True)
                        shutil.copy2(subitem, dest_file)


def copy_gitignore(project_path: Path, templates_dir: Path) -> None:
    """Copy .gitignore template if it exists and one doesn't already exist."""
    gitignore_src = templates_dir / ".gitignore"
    gitignore_dst = project_path / ".gitignore"

    if gitignore_src.exists() and not gitignore_dst.exists():
        shutil.copy2(gitignore_src, gitignore_dst)
        log("INFO", "Copied .gitignore to project root")


