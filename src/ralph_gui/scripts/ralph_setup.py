def init_git(project_path: Path) -> None:
    """Initialize git repository if not already initialized."""
    if not (project_path / ".git").exists():
        subprocess.run(["git", "init"], cwd=project_path, check=False, capture_output=True)
        log("INFO", "Initialized git repository")

        # Create initial commit
        readme_path = project_path / "README.md"
        if not readme_path.exists():
            readme_path.write_text(f"# {project_path.name}\n", encoding="utf-8")

        subprocess.run(["git", "add", "."], cwd=project_path, check=False, capture_output=True)
        subprocess.run(
            ["git", "commit", "-m", "Initial Ralph project setup"],
            cwd=project_path,
            check=False,
            capture_output=True
        )
        log("INFO", "Created initial git commit")


def setup_project(project_name: str, templates_dir: Path = None) -> Path:
    """Main setup function."""
    project_path = Path.cwd() / project_name

    log("INFO", f"Setting up Ralph project: {project_name}")

    # Create project directory
    project_path.mkdir(exist_ok=True)

    # Find templates directory
    templates = find_templates_dir(templates_dir)
    if not templates:
        log("ERROR", "Templates directory not found.")
        log("ERROR", "Expected at: --templates-dir, ../templates, or ~/.ralph/templates")
        log("ERROR", "Please run install.sh first to install Ralph globally.")
        sys.exit(1)

    log("INFO", f"Using templates from: {templates}")

    # Verify templates
    if not verify_templates(templates):
        sys.exit(1)

    # Determine lib directory
    lib_dir = None
    if (Path.cwd().parent / "lib").exists():
        lib_dir = Path.cwd().parent / "lib"
    elif (Path.home() / ".ralph" / "lib").exists():
        lib_dir = Path.home() / ".ralph" / "lib"

    # Create directory structure
    create_project_structure(project_path)

    # Copy templates
    copy_templates(project_path, templates)

    # Copy .gitignore
    copy_gitignore(project_path, templates)

    # Create .ralphrc
    create_ralphrc(project_path, project_name, lib_dir)

    # Initialize git
    init_git(project_path)

    log("SUCCESS", f"Project {project_name} created!")

    return project_path


def main():
    parser = argparse.ArgumentParser(
        description="Ralph Setup - Create new Ralph project",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  ralph_setup.py my-project
  ralph_setup.py my-project --templates-dir /path/to/templates
        """
    )
    parser.add_argument(
        "project_name",
        nargs="?",
        default="my-project",
        help="Name for the new Ralph project (default: my-project)"
    )
    parser.add_argument(
        "--templates-dir",
        type=Path,
        default=None,
        help="Path to templates directory"
    )

    args = parser.parse_args()

    try:
        project_path = setup_project(args.project_name, args.templates_dir)

        print("\nNext steps:")
        print(f"  1. Edit .ralph/PROMPT.md with your project requirements")
        print("  2. Update .ralph/specs/ with your project specifications")
        print("  3. Run: ralph --monitor")
    except Exception as e:
        log("ERROR", f"Setup failed: {e}")
        sys.exit(1)


if __name__ == "__main__":
    main()
